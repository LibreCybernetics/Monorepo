class Simple:
  @Benchmark
  def main(): Unit =
    val Right(toml)      = readTOML[Either[Parser.Error, _]](Simple.sample): @unchecked
    val Right(structured) = Simple.wholeTOMLDecoder.decode(toml): @unchecked
    assert(structured.author.name == "Jane Doe")
    assert(structured.author.email == "jane.doe@example.com")
